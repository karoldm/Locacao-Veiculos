    private void TextIDClienteFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_TextIDClienteFocusLost
        // TODO add your handling code here:
        if(TextIDCliente.getText().equals("")){
            LabelErroIDCliente.setText("Este campo precisa ser preenchido!");
            LabelErroIDCliente.setVisible(true);
            TextIDCliente.setText("");
            TextIDCliente.requestFocus();
        }
        else {
            try {
                boolean clienteCadastrado = false;
                for(Usuario u: controller.getUsuarios()){
                    if(u instanceof Cliente
                    && u.getCodigoUsuario() == Integer.parseInt(TextIDCliente.getText())){
                        clienteCadastrado = true;
                        Cliente c = (Cliente)u;
                        TextNomeCliente.setText(c.getNome());
                        TextCPF.setText(c.getCpf());
                        TextRG.setText(c.getRg());
                        TextCEP.setText(c.getCep());
                        TextEndereco.setText(c.getEndereco());
                        TextEmail.setText(c.getEmail());
                        TextCategoriaCNH.setText(c.getCategoriaCNH());
                        TextNumeroCNH.setText(c.getNumeroCNH());
                        TextValidadeCNH.setText(c.getValidadeCNH().get(Calendar.DATE) 
                            + "-" + c.getValidadeCNH().get(Calendar.MONTH)
                            + "-" + c.getValidadeCNH().get(Calendar.YEAR));
                        CheckBoxClienteOuro.setSelected(c.getClienteOuro());
                        break;
                    }
                }
                if(clienteCadastrado == false){
                    LabelErroIDCliente.setText("Cliente não cadastrado!");
                    LabelErroIDCliente.setVisible(true);
                    TextIDCliente.setText("");
                    TextIDCliente.requestFocus();
                }
                else {
                    LabelErroIDCliente.setVisible(false);
                }
            } catch(NumberFormatException nfe){
                    LabelErroIDCliente.setText("Entrada inválida!");
                    LabelErroIDCliente.setVisible(true);
                    TextIDCliente.setText("");
                    TextIDCliente.requestFocus();
                }
        }
    }//GEN-LAST:event_TextIDClienteFocusLost

    private void ComboBoxCarrosDispActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComboBoxCarrosDispActionPerformed
        // TODO add your handling code here:
        String id = ComboBoxCarrosDisp.getSelectedItem().toString();
        id = id.split(" - ")[1];
       
        for(Veiculo v: veiculos){
            if(id.equals(Integer.toString(v.getCodigoVeiculo()))){
                this.veiculo = v;
                break;
            }
        }
        
        TextModelo.setText(this.veiculo.getNomeModelo());
        TextMontadora.setText(this.veiculo.getMontadora());
        TextCategoria.setText(this.veiculo.getCategoria());
        TextValorDiaria.setText(Float.toString(this.veiculo.getValorDiaria()));
        TextAnoFabricacao.setText(Integer.toString(this.veiculo.getAnoFabricacao()));
        TextAnoModelo.setText(Integer.toString(this.veiculo.getAnoModelo()));
        TextPlaca.setText(this.veiculo.getPlaca());
        
        //recalculando dias de locação para o novo valor da diária
        if(this.dataLocacao != null){
            int dias = Utilidades.daysBetween(dataLocacao, dataDevolucao);
            this.total = this.totalSeguros
                    + dias * this.veiculo.getValorDiaria();
            
            ValorTotal.setText(String.format("%.2f", this.total));
        }
        
    }//GEN-LAST:event_ComboBoxCarrosDispActionPerformed

