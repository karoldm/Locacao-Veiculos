    private void ButtonConfirmDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonConfirmDataActionPerformed
        // TODO add your handling code here:
        String diaLocacao = TextDiaLocacao.getText();
        String mesLocacao = TextMesLocacao.getText();
        String anoLocacao = TextAnoLocacao.getText();
        String diaDevolucao = TextDiaDevolucao.getText();
        String mesDevolucao = TextMesDevolucao.getText();
        String anoDevolucao = TextAnoDevolucao.getText();

        String[] itens = {
            diaLocacao,
            mesLocacao,
            anoLocacao,
            diaDevolucao,
            mesDevolucao,
            anoDevolucao
        };
        
        if(!Utilidades.hasNull(itens)){
            try {
                this.dataLocacao = Utilidades.getValidDate(
                    diaLocacao,
                    mesLocacao,
                    anoLocacao);
                this.dataDevolucao = Utilidades.getValidDate(
                    diaDevolucao,
                    mesDevolucao,
                    anoDevolucao);
            
                if(dataLocacao != null && dataDevolucao != null){
                    int diasAlugado = Utilidades.daysBetween(dataLocacao, dataDevolucao);
                    
                    this.total = this.totalSeguros +
                            diasAlugado * this.veiculo.getValorDiaria();
                    
                    ValorTotal.setText(String.format("%.2f", this.total));
                    
                    ButtonAlugarCarro.setEnabled(true);
                    ButtonAlugarCarro.setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));
                } //if validDate
                else {
                    JOptionPane.showMessageDialog(this, "Formato inválido."
                        + " Dia, mês ou ano inserido são inválidos!",
                    "Atenção", JOptionPane.WARNING_MESSAGE);
                    TextDiaLocacao.setText("");
                    TextMesLocacao.setText("");
                    TextAnoLocacao.setText("");
                    TextDiaDevolucao.setText("");
                    TextMesDevolucao.setText("");
                    TextAnoDevolucao.setText("");
                }
            } catch (NumberFormatException nfe){
                JOptionPane.showMessageDialog(this, "Dados inválidos."
                        + " Dia, mês e ano devem ser números inteiros!",
                    "Atenção", JOptionPane.WARNING_MESSAGE);
                    TextDiaLocacao.setText("");
                    TextMesLocacao.setText("");
                    TextAnoLocacao.setText("");
                    TextDiaDevolucao.setText("");
                    TextMesDevolucao.setText("");
                    TextAnoDevolucao.setText("");
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Campos não preenchidos."
                        + " Por favor preencha todos os campos de data!",
                    "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_ButtonConfirmDataActionPerformed
    
