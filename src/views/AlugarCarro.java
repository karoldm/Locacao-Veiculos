    private void ButtonAlugarCarroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAlugarCarroActionPerformed
        // TODO add your handling code here:
        String codigoCliente = TextIDCliente.getText();
        String codigoFuncionario = TextIDFuncionario.getText();
        String nomeCartao = TextNomeCartao.getText();
        String bandeiraCartao = TextBandeiraCartao.getText();
        String numeroCartao = TextNumCartao.getText();
        Pagamento pagamento;
        
        String[] itens = {
            codigoCliente,
            codigoFuncionario,
            (RadioButtonCartao.isSelected()?nomeCartao:"1"),
            (RadioButtonCartao.isSelected()?bandeiraCartao:"1"),
            (RadioButtonCartao.isSelected()?numeroCartao:"1")
        };
        
        if(!Utilidades.hasNull(itens)){
            if(RadioButtonDinheiro.isSelected()){
                pagamento = new Dinheiro();
            }
            else {
                pagamento = new Cartao(nomeCartao, bandeiraCartao, numeroCartao);
            }

            try {
            boolean funcionarioCadastrado = false;
                for(Usuario u: controller.getUsuarios()){
    
                    if(u instanceof Funcionario
                   && u.getCodigoUsuario() == Integer.parseInt(codigoFuncionario)){
                        funcionarioCadastrado = true;
                        break;
                    }
                }
                if(funcionarioCadastrado){
                    Locacao l = new Locacao(
                        this.id,
                        Integer.parseInt(codigoCliente),
                        Integer.parseInt(codigoFuncionario),
                        this.veiculo, 
                        this.dataLocacao, 
                        this.dataDevolucao,
                        this.total,
                        pagamento,
                        this.seguros,
                        false
                    );
                    
                    this.veiculo.alugar();
                    veiculos.removeIf(c -> 
                            this.veiculo.getCodigoVeiculo() == c.getCodigoVeiculo());
                    this.veiculos.add(this.veiculo);
                    controller.setVeiculos(this.veiculos);
            
                    controller.addLocacao(l);
                    locacoes.add(l);
                    
                    JOptionPane.showMessageDialog(this, "Locação finalizada."
                        + " Locação realizada com sucesso!",
                    "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                    
                    clearForm();
                }
                else {
                    JOptionPane.showMessageDialog(this, "Funcionário não cadastrado."
                        + " Não há nenhum funcionário cadastrado com esse código!",
                    "Atenção", JOptionPane.WARNING_MESSAGE);
                }
            }catch(NumberFormatException nfe){
                JOptionPane.showMessageDialog(this, "Entrada de dado inválida."
                    + " O código do funcionário deve ser um valor inteiro!",
                "Atenção", JOptionPane.WARNING_MESSAGE);
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Entrada de dado vazia."
                    + " Por favor preencha todos os campos necessários!",
                "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_ButtonAlugarCarroActionPerformed

